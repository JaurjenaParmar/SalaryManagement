    private void consultantsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consultantsActionPerformed
        try {
            // TODO add your handling code here:
            name = (String) consultants.getSelectedItem();
            if (consultants.getSelectedIndex() != 0) {
                String query = "SELECT ID from users where name=? AND role=?";
                int role = 2;
                PreparedStatement ps = connectionClass.connection.prepareStatement(query);
                ps.setString(1, name);
                ps.setInt(2, role);
                ResultSet rs = ps.executeQuery();
                while (rs.next()) {
                    id.setText(String.valueOf(rs.getInt("ID")));
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(GiveSalaryShare.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_consultantsActionPerformed

