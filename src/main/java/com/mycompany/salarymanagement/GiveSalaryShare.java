    private void btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActionPerformed
        // TODO add your handling code here:
        if (consultants.getSelectedIndex() == 0) {
            consultant.setText(" !! Select Consultant name");
            consultant.setVisible(true);
        } else if (salary.getText().isEmpty()) {
            share1.setText("Enter Salary share!!");
            share1.setVisible(true);
        } else if(isUpdate ==0) {
            String query = "INSERT INTO MANAGE_SALARY(ManagerID,"
                    + "consultantName,"
                    + "consultantID,"
                    + "Date,"
                    + "SalaryShare) "
                    + "VALUES(?,?,?,?,?)";
            PreparedStatement ps = null;
            try {
                ps = connectionClass.connection.prepareStatement(query);

                ps.setInt(1, data.getId());
                ps.setString(2, name);
                ps.setInt(3, consultantID);
                ps.setString(4, date.getText());
                ps.setDouble(5, Double.parseDouble(salary.getText()));
                ps.execute();
                JOptionPane.showMessageDialog(null, "Salary share of " + Double.parseDouble(salary.getText()) + " given to " + name + " successfully");
                ManagerHome home = new ManagerHome(data);
                home.setVisible(true);
                this.dispose();

            } catch (SQLException ex) {
                Logger.getLogger(AddConsultant.class
                        .getName()).log(Level.SEVERE, null, ex);
            } catch (ParseException ex) {
                Logger.getLogger(AddConsultant.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else if(isUpdate!=0){
            String query = "UPDATE MANAGE_SALARY set ManagerID=?, consultantName=?, consultantID=?, Date=?, SalaryShare=? WHERE ID=?";
            PreparedStatement ps = null;
            try {
                ps = connectionClass.connection.prepareStatement(query);

                ps.setInt(1, data.getId());
                ps.setString(2, name);
                ps.setInt(3, consultantID);
                ps.setString(4, date.getText());
                ps.setDouble(5, Double.parseDouble(salary.getText()));
                ps.execute();
                JOptionPane.showMessageDialog(null, "Salary share of " + Double.parseDouble(salary.getText()) + " given to " + name + "  updated successfully");
                ManagerHome home = new ManagerHome(data);
                home.setVisible(true);
                this.dispose();

            } catch (SQLException ex) {
                Logger.getLogger(AddConsultant.class
                        .getName()).log(Level.SEVERE, null, ex);
            } catch (ParseException ex) {
                Logger.getLogger(AddConsultant.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnActionPerformed
