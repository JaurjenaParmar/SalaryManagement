    private void deletebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletebtnActionPerformed
        // TODO add your handling code here:
        if (ViewEmployees.tableClicked == true) {
            String statement = "DELETE FROM users WHERE ID=?";
            PreparedStatement ps = null;
            try {
                ps = connectionClass.connection.prepareStatement(statement);
                ps.setInt(1, employeeData.getId());

                if (ps.executeUpdate() > 0) {
                    JOptionPane.showMessageDialog(null, "Employee '" + employeeData.getName() + "' Removed Succesfully");
                    ViewEmployees view = new ViewEmployees(edata);
                    view.setVisible(true);
                    this.dispose();

                }
            } catch (SQLException ex) {
                Logger.getLogger(ViewEmployees.class
                        .getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Error!");
        }
    }//GEN-LAST:event_deletebtnActionPerformed

