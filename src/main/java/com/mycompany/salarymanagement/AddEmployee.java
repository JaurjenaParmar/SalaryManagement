    private void contactKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_contactKeyTyped
        // TODO add your handling code here:
        contact1.setVisible(false);
        keyTyped++;
        char ch = evt.getKeyChar();
        if (ch == KeyEvent.VK_BACK_SPACE || Character.isDigit(ch)) {
        } else {
            contact1.setText("Invalid Charcter '" + ch + "'");
            contact1.setVisible(true);
            evt.consume();
        }
    }//GEN-LAST:event_contactKeyTyped
